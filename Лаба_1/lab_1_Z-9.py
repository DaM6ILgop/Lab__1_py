# Создаем словарь с купюрами разного достоинства и их количеством
bills = {
    1000: 10,
    500: 20,
    100: 50,
    50: 100,
    10: 200
}

# Запрашиваем у пользователя сумму денег для снятия
amount = int(input("Введите сумму, которую вы хотите снять: "))

# Создаем словарь для хранения количества купюр, которые нужно выдать
result = {}

# Проходим по каждой купюре в словаре bills
for bill in sorted(bills.keys(), reverse=True):
    # Вычисляем, сколько купюр этого достоинства можно выдать
    count = min(amount // bill, bills[bill])
    # Добавляем количество купюр в словарь result
    result[bill] = count
    # Вычитаем сумму, соответствующую выданным купюрам, из общей суммы
    amount -= count * bill

# Если после подсчета купюр осталась какая-то сумма, значит недостаточно денег в банкомате
if amount != 0:
    print("Операция не может быть выполнена!")
else:
    # Форматируем вывод в нужном формате
    output = " + ".join([f"{count}*{bill}" for bill, count in result.items()])
    print(output)